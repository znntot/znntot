public class BitQuark {
		public int bit; // 0 or 1
		public String color; // Red, Green, Blue
		public String charge; // +, -, 0
		public String spin; // up, down

		public BitQuark(int bit, String color, String charge, String spin) {
			this.bit = bit;
			this.color = color;
			this.charge = charge;
			this.spin = spin;
		}
	}